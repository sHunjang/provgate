// ============================================================
// AI가 생성한 텍스트에서 마크다운 코드펜스(```) 마커를 제거하는 공용 유틸
// ============================================================
// 왜 공용으로 뺐나:
//   그동안 이 로직이 GateModal, onboarding/quiz, onboarding/result 등
//   여러 페이지에 .replace(/```python/g,"").replace(/```/g,"") 형태로
//   '복붙'되어 흩어져 있었다. 그래서 처리를 빠뜨린 페이지
//   (learn/[track]/[id] 문제풀이 화면 등)에서 마크다운 펜스가 날것으로
//   화면에 노출됐다. 같은 일을 여러 곳에서 제각각 하니 한 곳을 고쳐도
//   나머지가 안 고쳐지는 두더지잡기가 됐음.
//
//   해결: 이 함수 하나로 통일하고, 코드펜스를 다루는 모든 페이지가
//   여기서 import해서 쓴다. (DRY — 표시 로직은 한 곳에서만 관리)
//
// 주의: 이건 '벗겨서 평문으로 보여주는' 임시 방식이다. 코드가 코드답게
//   (들여쓰기/폰트) 보이려면 <pre><code> 렌더링이 맞지만, 그건 데모에
//   필수가 아니라 다음 단계로 미룬다. 지금은 '펜스/언어명 잔여물이
//   화면에 안 보이게' 하는 선까지만 처리한다.

/**
 * AI 텍스트에서 마크다운 코드펜스와 언어명 잔여물을 제거한다.
 *
 * 처리하는 변형들:
 *   ```python\n코드```      (언어명 + 바로 줄바꿈)
 *   ```py 코드```           (짧은 언어명)
 *   ```\npython\n코드```    (펜스 다음 줄에 언어명이 따로)
 *   ``` 코드 ```            (언어명 없음)
 */
export function stripCodeFence(text: string | null | undefined): string {
    if (!text) return "";
    return (
        text
            // ```python / ```py / ```Python 등 여는·닫는 펜스 + 붙은 언어명을 통째로 제거
            // (```로 시작하고 뒤에 영문 언어명이 0개 이상 붙는 형태)
            .replace(/```[a-zA-Z]*/g, "")
            // 위에서 펜스는 지웠지만, 펜스 없이 "python" 단어만 한 줄로 남는
            // 케이스가 있어서, 줄 전체가 언어명 하나뿐인 줄을 지운다.
            // (^\s*  줄 시작 공백,  (python|py|javascript|js) 언어명,  \s*$  줄 끝 공백)
            .replace(/^\s*(python|py|javascript|js)\s*$/gim, "")
            .trim()
    );
}
